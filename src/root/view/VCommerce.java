    private void btConfirmRequestRegistrationActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btConfirmRequestRegistrationActionPerformed
        //Caso a opção Novo Cliente esteja marcada, este codigo é executado
        if (rdbtNewClient.isSelected()) {
            //Recolhe as informações dos campos de texto do registro
            String name = txtClientName.getText();
            String cpf = fmttxtClientCpf.getText();
            String cep = fmttxtClientCep.getText();
            String email = txtClientEmail.getText();
            String telephone = fmttxtClientTelephone.getText();
            
            //Chama o controlador para criar o cliente e abrir a requisição
            if(this.ctrl.registerClient(name, cpf, cep, email, telephone))
                ctrl.createRequest(ctrl.createClient(name, cpf, cep, email, telephone));
            //Recolhe a quantidade de produtos selecionados
            int productQuantity = lstSelectedProductList.getModel().getSize();
            //Armazena os produtos selecionados na requisição
            for(int counter = 0; counter < productQuantity; counter++){
                ctrl.addProductToRequest(ctrl.stringToProduct(lstSelectedProductList.getModel().getElementAt(counter)));
            }
            //Se conseguir finalizar a requisição ele retorna uma mensagem de 
            //sucesso e volta ao menu de requisições
            //Senão, retorna uma mensagem de erro.
            if(ctrl.finishRequest()){
                goToPanel("RequestMenu");
                JOptionPane.showMessageDialog(pnlFatherPanel, "Sucesso!");
            } else {
                JOptionPane.showMessageDialog(pnlFatherPanel, "Um erro aconteceu!");
            } 
        }
        //Caso a opção Cliente Registrado esteja marcada, este codigo é executado
        if (rdbtRegisteredClient.isSelected()) {
            //Recolhe as informações direto do log de clientes direto da classe de controle
            int index = cbClientSelection.getSelectedIndex();
            if (index < 0) {
                index = 0;
            }
            String name = this.ctrl.giveClientArray()[index].getName();
            String cpf = this.ctrl.giveClientArray()[index].getCpf();
            String cep = this.ctrl.giveClientArray()[index].getCep();
            String email = this.ctrl.giveClientArray()[index].getEmail();
            String telephone = this.ctrl.giveClientArray()[index].getTelephone();
            
            //Chama o controlador para criar o cliente e abrir a requisição
            ctrl.createRequest(ctrl.createClient(name, cpf, cep, email, telephone));
            //Recolhe a quantidade de produtos selecionados
            int productQuantity = lstSelectedProductList.getModel().getSize();
            //Armazena os produtos selecionados na requisição
            for(int counter = 0; counter < productQuantity; counter++){
                ctrl.addProductToRequest(ctrl.stringToProduct(lstSelectedProductList.getModel().getElementAt(counter)));
            }
            //Se conseguir finalizar a requisição ele retorna uma mensagem de 
            //sucesso e volta ao menu de requisições
            //Senão, retorna uma mensagem de erro.
            if(ctrl.finishRequest()){
                goToPanel("RequestMenu");
                JOptionPane.showMessageDialog(pnlFatherPanel, "Sucesso!");
            } else {
                JOptionPane.showMessageDialog(pnlFatherPanel, "Um erro aconteceu!");
            }   
        }
    }//GEN-LAST:event_btConfirmRequestRegistrationActionPerformed

